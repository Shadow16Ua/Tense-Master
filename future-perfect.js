function checkAnswers() {
    let score = 0;
    const totalQuestions = 5;

    const q1 = document.querySelector('input[name="q1"]:checked');
    const q2 = document.querySelector('input[name="q2"]:checked');
    const q3 = document.querySelector('input[name="q3"]:checked');
    const q4 = document.querySelector('input[name="q4"]:checked');
    const q5 = document.querySelector('input[name="q5"]:checked');

    if (q1 && q1.value === "will have finished") score++;
    if (q2 && q2.value === "will have eaten") score++;
    if (q3 && q3.value === "arrive") score++; // Пастка! Present Simple після By the time
    if (q4 && q4.value === "won't have written") score++;
    if (q5 && q5.value === "Will you have fixed") score++; 

    const resultDiv = document.getElementById("quizResult");
    const currentLangText = document.getElementById('currentLang').innerText;
    let successMsg, errorMsg, incompleteMsg;

    if (currentLangText.includes("Українська")) {
        successMsg = `Супер! ${score}/${totalQuestions} правильних відповідей.`;
        errorMsg = `Треба повторити правила. ${score}/${totalQuestions} правильних.`;
        incompleteMsg = "Будь ласка, дай відповідь на всі 5 запитань!";
    } else if (currentLangText.includes("Polski")) {
        successMsg = `Świetnie! ${score}/${totalQuestions} poprawnych.`;
        errorMsg = `Musisz powtórzyć zasady. ${score}/${totalQuestions} poprawnych.`;
        incompleteMsg = "Odpowiedz na wszystkie 5 pytań!";
    } else if (currentLangText.includes("Русский")) {
        successMsg = `Супер! ${score}/${totalQuestions} правильных.`;
        errorMsg = `Надо повторить правила. ${score}/${totalQuestions} правильных.`;
        incompleteMsg = "Пожалуйста, ответь на все 5 вопросов!";
    } else {
        successMsg = `Awesome! ${score}/${totalQuestions} correct.`;
        errorMsg = `Review the rules. ${score}/${totalQuestions} correct.`;
        incompleteMsg = "Please answer all 5 questions!";
    }

    if (!q1 || !q2 || !q3 || !q4 || !q5) {
        resultDiv.textContent = incompleteMsg;
        resultDiv.className = "quiz-result result-error";
        return;
    }

    if (score === totalQuestions) {
        resultDiv.textContent = successMsg;
        resultDiv.className = "quiz-result result-success";

        localStorage.setItem("tense_completed_future-perfect", "true");
    } else {
        resultDiv.textContent = errorMsg;
        resultDiv.className = "quiz-result result-error";
    }
}
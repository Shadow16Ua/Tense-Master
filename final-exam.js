function checkExamAnswers() {
    let score = 0;
    const totalQuestions = 24;
    let allAnswered = true;

    // Знімаємо ховери
    document.getElementById('quizContainer').classList.remove('quiz-active');

    // Очищаємо попередні результати
    document.querySelectorAll('.radio-box').forEach(el => {
        el.classList.remove('correct-answer', 'wrong-answer');
    });

    // Перевіряємо кожне питання
    for (let i = 1; i <= totalQuestions; i++) {
        const qName = 'q' + i;
        const selected = document.querySelector(`input[name="${qName}"]:checked`);
        
        if (!selected) {
            allAnswered = false;
        } else {
            const selectedBox = selected.nextElementSibling;
            
            // Перевіряємо з об'єктом examAnswers
            if (selected.value === examAnswers[qName]) {
                score++;
                selectedBox.classList.add('correct-answer');
            } else {
                selectedBox.classList.add('wrong-answer');
                const correctInput = document.querySelector(`input[name="${qName}"][value="${examAnswers[qName]}"]`);
                if (correctInput) {
                    correctInput.nextElementSibling.classList.add('correct-answer');
                }
            }
            document.querySelectorAll(`input[name="${qName}"]`).forEach(input => input.disabled = true);
        }
    }

    const resultDiv = document.getElementById("quizResult");

    // НАДІЙНИЙ СПОСІБ ЗНАЙТИ МОВУ (Шукаємо назву прямо в кнопці меню)
    const btnContent = document.getElementById('currentLang').innerHTML;
    const isUk = btnContent.includes("Українська") || btnContent.includes("ua.webp");
    const isPl = btnContent.includes("Polski") || btnContent.includes("pl.webp");
    const isRu = btnContent.includes("Русский") || btnContent.includes("ru.webp");

    // Якщо користувач пропустив питання
    if (!allAnswered) {
        document.getElementById('quizContainer').classList.add('quiz-active');
        document.querySelectorAll('input[type="radio"]').forEach(input => input.disabled = false);
        
        if (isUk) {
            resultDiv.textContent = "Будь ласка, дай відповідь на всі 24 запитання!";
        } else if (isPl) {
            resultDiv.textContent = "Odpowiedz na wszystkie 24 pytania!";
        } else if (isRu) {
            resultDiv.textContent = "Пожалуйста, ответь на все 24 вопроса!";
        } else {
            resultDiv.textContent = "Please answer all 24 questions!";
        }
        resultDiv.className = "quiz-result result-error";
        return;
    }

    // Якщо тест пройдено повністю – видаємо фінальний результат тією ж мовою
    let finalMsg = "";
    if (score === 24) {
        if (isUk) finalMsg = `🏆 БЕЗДОГАННО! ${score}/${totalQuestions}. Ти Бог граматики!`;
        else if (isPl) finalMsg = `🏆 PERFEKCYJNIE! ${score}/${totalQuestions}. Jesteś Bogiem Gramatyki!`;
        else if (isRu) finalMsg = `🏆 БЕЗУПРЕЧНО! ${score}/${totalQuestions}. Ты Бог грамматики!`;
        else finalMsg = `🏆 FLAWLESS! ${score}/${totalQuestions}. You are a Grammar God!`;
        resultDiv.className = "quiz-result result-success";
        
    } else if (score >= 18) {
        if (isUk) finalMsg = `🔥 Дуже сильно! ${score}/${totalQuestions}. Майже ідеально.`;
        else if (isPl) finalMsg = `🔥 Bardzo mocno! ${score}/${totalQuestions}. Prawie idealnie.`;
        else if (isRu) finalMsg = `🔥 Очень сильно! ${score}/${totalQuestions}. Почти идеально.`;
        else finalMsg = `🔥 Very strong! ${score}/${totalQuestions}. Almost perfect.`;
        resultDiv.className = "quiz-result result-success";
        
    } else {
        if (isUk) finalMsg = `📚 Ще є над чим працювати. Результат: ${score}/${totalQuestions}.`;
        else if (isPl) finalMsg = `📚 Jeszcze jest nad czym pracować. Wynik: ${score}/${totalQuestions}.`;
        else if (isRu) finalMsg = `📚 Еще есть над чем работать. Результат: ${score}/${totalQuestions}.`;
        else finalMsg = `📚 Still room for improvement. Score: ${score}/${totalQuestions}.`;
        resultDiv.className = "quiz-result result-error";
    }

    resultDiv.textContent = finalMsg;
    
    // Ховаємо кнопку після перевірки
    document.querySelector('.quiz-btn').style.display = 'none';
}
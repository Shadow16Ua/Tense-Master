function loadProgress() {
    // Список усіх ID наших часів (мають збігатися з ID галочок)
    const tenses = [
        "present-simple", "present-continuous", "present-perfect", "present-perfect-continuous",
        "past-simple", "past-continuous", "past-perfect", "past-perfect-continuous",
        "future-simple", "future-continuous", "future-perfect", "future-perfect-continuous"
    ];

    let completedCount = 0;

    tenses.forEach(tense => {
        // Читаємо з кешу браузера
        if (localStorage.getItem(`tense_completed_${tense}`) === "true") {
            const checkmark = document.getElementById(`check-${tense}`);
            if (checkmark) {
                checkmark.classList.add('completed');
                completedCount++;
            }
        }
    });

    // Можна вивести в консоль для перевірки
    console.log(`Вивчено часів: ${completedCount} з 12`);
}

// Запускаємо при завантаженні сторінки
window.addEventListener('DOMContentLoaded', loadProgress);

// Логіка для перевірки тесту
function checkAnswers() {
    let score = 0;
    const totalQuestions = 3;

    // Отримуємо вибрані відповіді
    const q1 = document.querySelector('input[name="q1"]:checked');
    const q2 = document.querySelector('input[name="q2"]:checked');
    const q3 = document.querySelector('input[name="q3"]:checked');

    // Правильні відповіді: 1 - goes, 2 - don't like, 3 - Do
    if (q1 && q1.value === "goes") score++;
    if (q2 && q2.value === "don't like") score++;
    if (q3 && q3.value === "Do") score++;

    const resultDiv = document.getElementById("quizResult");
    
    // Перевіряємо поточну мову (по тексту на кнопці)
    const currentLangText = document.getElementById('currentLang').innerText;
    let successMsg, errorMsg, incompleteMsg;

    if (currentLangText.includes("Українська")) {
        successMsg = `Супер! ${score}/${totalQuestions} правильних відповідей.`;
        errorMsg = `Треба повторити правила. ${score}/${totalQuestions} правильних.`;
        incompleteMsg = "Будь ласка, дай відповідь на всі запитання!";
    } else if (currentLangText.includes("Polski")) {
        successMsg = `Świetnie! ${score}/${totalQuestions} poprawnych odpowiedzi.`;
        errorMsg = `Musisz powtórzyć zasady. ${score}/${totalQuestions} poprawnych.`;
        incompleteMsg = "Proszę odpowiedzieć na wszystkie pytania!";
    } else if (currentLangText.includes("Русский")) {
        successMsg = `Супер! ${score}/${totalQuestions} правильных ответов.`;
        errorMsg = `Надо повторить правила. ${score}/${totalQuestions} правильных.`;
        incompleteMsg = "Пожалуйста, ответь на все вопросы!";
    } else {
        successMsg = `Awesome! ${score}/${totalQuestions} correct answers.`;
        errorMsg = `Review the rules. ${score}/${totalQuestions} correct.`;
        incompleteMsg = "Please answer all questions!";
    }

    if (!q1 || !q2 || !q3) {
        resultDiv.textContent = incompleteMsg;
        resultDiv.className = "quiz-result result-error";
        return;
    }

    if (score === totalQuestions) {
        resultDiv.textContent = successMsg;
        resultDiv.className = "quiz-result result-success";
    } else {
        resultDiv.textContent = errorMsg;
        resultDiv.className = "quiz-result result-error";
    }
}




